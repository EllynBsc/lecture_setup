# 1.Check your status
git status

# 2.Select the files to be committed
git add file_1_which_has_been_modified_or_created
git add file_2_which_has_been_modified_or_created
# or select all the files in current directory
git add .  (shortcut: ga .)

# 3. Commit your work with a meaningfull message
git commit -m "Why I did this change" (shortcut: gc -m "message")

# 4.Push your exercise to kitt/github with good style
git push origin master
# => Make sure your git status is clean before you go on a new exercise or you leave your computer at night

